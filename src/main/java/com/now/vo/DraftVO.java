package com.now.vo;

public class DraftVO {
	
	private String draft_no;
	private String draft_name;
	private String draft_content;
	private String draft_category;
	private String draft_del;
	
	public String getDraft_no() {
		return draft_no;
	}
	public void setDraft_no(String draft_no) {
		this.draft_no = draft_no;
	}
	public String getDraft_name() {
		return draft_name;
	}
	public void setDraft_name(String draft_name) {
		this.draft_name = draft_name;
	}
	public String getDraft_content() {
		return draft_content;
	}
	public void setDraft_content(String draft_content) {
		this.draft_content = draft_content;
	}
	public String getDraft_category() {
		return draft_category;
	}
	public void setDraft_category(String draft_category) {
		this.draft_category = draft_category;
	}
	public String getDraft_del() {
		return draft_del;
	}
	public void setDraft_del(String draft_del) {
		this.draft_del = draft_del;
	}
	
}
