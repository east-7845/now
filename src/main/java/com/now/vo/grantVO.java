package com.now.vo;

public class grantVO {
	
	private String grant_app_no;
	private String grant_emp_no;
	private int grant_rank;
	private String grant_state;
	private String grant_return_content;
	
	public String getGrant_app_no() {
		return grant_app_no;
	}
	public void setGrant_app_no(String grant_app_no) {
		this.grant_app_no = grant_app_no;
	}
	public String getGrant_emp_no() {
		return grant_emp_no;
	}
	public void setGrant_emp_no(String grant_emp_no) {
		this.grant_emp_no = grant_emp_no;
	}
	public int getGrant_rank() {
		return grant_rank;
	}
	public void setGrant_rank(int grant_rank) {
		this.grant_rank = grant_rank;
	}
	public String getGrant_state() {
		return grant_state;
	}
	public void setGrant_state(String grant_state) {
		this.grant_state = grant_state;
	}
	public String getGrant_return_content() {
		return grant_return_content;
	}
	public void setGrant_return_content(String grant_return_content) {
		this.grant_return_content = grant_return_content;
	}
	
}
