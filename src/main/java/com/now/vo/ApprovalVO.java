package com.now.vo;

public class ApprovalVO extends grantVO {
	
	private String app_no;
	private String app_emp_no;
	private String app_draft_no;
	private String app_name;
	private String app_content;
	private String app_state;
	private String app_del;
	public String getApp_no() {
		return app_no;
	}
	public void setApp_no(String app_no) {
		this.app_no = app_no;
	}
	public String getApp_emp_no() {
		return app_emp_no;
	}
	public void setApp_emp_no(String app_emp_no) {
		this.app_emp_no = app_emp_no;
	}
	public String getApp_draft_no() {
		return app_draft_no;
	}
	public void setApp_draft_no(String app_draft_no) {
		this.app_draft_no = app_draft_no;
	}
	public String getApp_name() {
		return app_name;
	}
	public void setApp_name(String app_name) {
		this.app_name = app_name;
	}
	public String getApp_content() {
		return app_content;
	}
	public void setApp_content(String app_content) {
		this.app_content = app_content;
	}
	public String getApp_state() {
		return app_state;
	}
	public void setApp_state(String app_state) {
		this.app_state = app_state;
	}
	public String getApp_del() {
		return app_del;
	}
	public void setApp_del(String app_del) {
		this.app_del = app_del;
	}
	
	
}
